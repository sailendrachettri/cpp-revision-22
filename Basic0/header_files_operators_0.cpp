#include <iostream>
using namespace std;

int main()
{
    int num1 = 4, num2 = 5;
    cout<<"Operators in CPP: "<<endl;
    cout<<"Arithmetic operator"<<endl;

    cout<<"The value of "<<num1<<" + "<<num2<<" is "<<num1 + num2<<endl;
    cout<<"The value of "<<num1<<" - "<<num2<<" is "<<num1 - num2<<endl;
    cout<<"The value of "<<num1<<" / "<<num2<<" is "<<num1 / num2<<endl;
    cout<<"The value of "<<num1<<" * "<<num2<<" is "<<num1 * num2<<endl;
    cout<<"The value of "<<num1<<" % "<<num2<<" is "<<num1 % num2<<endl;
    

    return 0;
}